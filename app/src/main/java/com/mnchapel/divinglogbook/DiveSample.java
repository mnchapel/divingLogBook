public class DiveSample {

    //
    float depth;

    //
    int time;



    /**
     * Constructor
     */
    public DiveSample() {

    }



    // GETTER --------------------------------------------------------------------------------------

    public float getDepth() {
        return depth;
    }

    public int getTime() {
        return time;
    }



    // SETTER --------------------------------------------------------------------------------------

    public void setDepth(float depth) {
        this.depth = depth;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
